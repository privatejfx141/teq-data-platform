package com.devlopp.teq.service.employment;

public class LongTermIntervention {

    protected String serviceRecieved;
    protected String status;
    protected String reasonForLeave;
    protected String startDate;
    protected String endDate;
    protected String employerSize;
    protected String placement;
    protected String averageHoursPerWeek;
    protected String metMentorAt;
    protected String hoursPerWeek;
    protected String profession;

    public String getServiceRecieved() {
        return serviceRecieved;
    }

    public String getStatus() {
        return status;
    }

    public String getReasonForLeave() {
        return reasonForLeave;
    }

    public String getStartDate() {
        return startDate;
    }

    public String getEndDate() {
        return endDate;
    }

    public String getEmployerSize() {
        return employerSize;
    }

    public String getPlacement() {
        return placement;
    }

    public String getAverageHoursPerWeek() {
        return averageHoursPerWeek;
    }

    public String getMetMentorAt() {
        return metMentorAt;
    }

    public String getHoursPerWeek() {
        return hoursPerWeek;
    }

    public String getProfession() {
        return profession;
    }

}
